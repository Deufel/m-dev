import numpy as np

def calculate_statistics(data):
    """Calculate basic statistics for a dataset

    Args:
        data: 

    """
    return {'mean': np.mean(data), 'median': np.median(data), 'std': np.std(data)}
def add(a: int, b: int) -> int:
    """Add `a` to `b`

    Args:
        a (int): 
        b (int): 

    Returns:
        int: 
    """
    return a + b

